資料集概覽
這份資料集包含 2002 年至 2024 年 中華民國（台灣）國民出國旅遊的統計數據，依 目的地 進行分類。

變數說明
首站抵達地（First Arrival Destination）：旅客的 第一個抵達地區（例如：亞洲、歐洲等）。
細分（Subdivision）：更細分的 國家或地區（例如：日本、韓國、香港）。
2002 - 2024：每年的出國人數。
變數類型
首站抵達地 和 細分 為 類別變數（文字型態）。
2002 - 2024 為 數值變數，但目前存為 文字格式（因為數字中含有逗號，例如 "1,280,853"），需要轉換為 數字型態（整數） 才能進行計算與分析。
變數種類（Variable Types）
名目變數（Nominal Variables）（分類、無序）
首站抵達地（First Arrival Destination）：不同地區名稱（如：亞洲、歐洲）。
細分（Subdivision）：特定國家或地區名稱（如：日本、韓國、香港）。
數值變數（Numerical Variables）（連續數據）
2002 - 2024 年的出國人數：每年的旅客人數，應為離散數據（Discrete Data），因為人數不會有小數點。